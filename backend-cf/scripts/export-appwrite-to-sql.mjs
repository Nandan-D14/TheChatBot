import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const REQUIRED_ENV = [
  "APPWRITE_ENDPOINT",
  "APPWRITE_PROJECT_ID",
  "APPWRITE_API_KEY",
  "APPWRITE_DB_ID",
  "APPWRITE_SESSIONS_COLLECTION_ID",
  "APPWRITE_MESSAGES_COLLECTION_ID",
  "APPWRITE_MEMORY_COLLECTION_ID",
];

function requireEnv(name) {
  const value = process.env[name];
  if (!value) {
    throw new Error(`Missing required environment variable: ${name}`);
  }
  return value;
}

function sqlEscape(value) {
  return `'${String(value ?? "").replace(/'/g, "''")}'`;
}

async function listDocuments({ endpoint, projectId, apiKey, dbId, collectionId }) {
  const normalizedEndpoint = endpoint.replace(/\/$/, "");
  const allDocuments = [];
  const limit = 100;
  let offset = 0;

  while (true) {
    const params = new URLSearchParams();
    params.append("queries[]", `limit(${limit})`);
    params.append("queries[]", `offset(${offset})`);

    const response = await fetch(
      `${normalizedEndpoint}/databases/${dbId}/collections/${collectionId}/documents?${params.toString()}`,
      {
        method: "GET",
        headers: {
          "X-Appwrite-Project": projectId,
          "X-Appwrite-Key": apiKey,
          "Content-Type": "application/json",
        },
      }
    );

    if (!response.ok) {
      const body = await response.text();
      throw new Error(
        `Appwrite listDocuments failed for collection ${collectionId}: ${response.status} ${body}`
      );
    }

    const payload = await response.json();
    const page = Array.isArray(payload.documents) ? payload.documents : [];

    allDocuments.push(...page);
    if (page.length < limit) {
      break;
    }

    offset += page.length;
  }

  return allDocuments;
}

function mapSession(doc) {
  return {
    id: doc.$id,
    user_id: doc.user_id ?? "shared-app-user",
    title: doc.title ?? "New Chat",
    created_at: doc.created_at ?? doc.$createdAt ?? new Date().toISOString(),
  };
}

function mapMessage(doc) {
  return {
    id: doc.$id,
    session_id: doc.session_id,
    role: doc.role ?? "assistant",
    content: doc.content ?? "",
    created_at: doc.created_at ?? doc.$createdAt ?? new Date().toISOString(),
  };
}

function mapMemory(doc) {
  const now = new Date().toISOString();
  return {
    id: doc.$id,
    user_id: doc.user_id ?? "shared-app-user",
    summary: doc.summary ?? "",
    created_at: doc.created_at ?? doc.$createdAt ?? now,
    updated_at: doc.updated_at ?? doc.$updatedAt ?? now,
  };
}

async function main() {
  for (const name of REQUIRED_ENV) {
    requireEnv(name);
  }

  const endpoint = requireEnv("APPWRITE_ENDPOINT");
  const projectId = requireEnv("APPWRITE_PROJECT_ID");
  const apiKey = requireEnv("APPWRITE_API_KEY");
  const dbId = requireEnv("APPWRITE_DB_ID");
  const sessionsCollectionId = requireEnv("APPWRITE_SESSIONS_COLLECTION_ID");
  const messagesCollectionId = requireEnv("APPWRITE_MESSAGES_COLLECTION_ID");
  const memoryCollectionId = requireEnv("APPWRITE_MEMORY_COLLECTION_ID");

  const [sessionsRaw, messagesRaw, memoryRaw] = await Promise.all([
    listDocuments({
      endpoint,
      projectId,
      apiKey,
      dbId,
      collectionId: sessionsCollectionId,
    }),
    listDocuments({
      endpoint,
      projectId,
      apiKey,
      dbId,
      collectionId: messagesCollectionId,
    }),
    listDocuments({
      endpoint,
      projectId,
      apiKey,
      dbId,
      collectionId: memoryCollectionId,
    }),
  ]);

  const sessions = sessionsRaw.map(mapSession);
  const messages = messagesRaw
    .map(mapMessage)
    .filter((message) => typeof message.session_id === "string" && message.session_id.length > 0);
  const memory = memoryRaw.map(mapMemory);

  const lines = [];
  lines.push("-- Generated by scripts/export-appwrite-to-sql.mjs");
  lines.push(`-- Generated at ${new Date().toISOString()}`);
  lines.push("PRAGMA foreign_keys = ON;");
  lines.push("BEGIN TRANSACTION;");

  for (const row of sessions) {
    lines.push(
      `INSERT OR REPLACE INTO sessions (id, user_id, title, created_at) VALUES (${sqlEscape(row.id)}, ${sqlEscape(row.user_id)}, ${sqlEscape(row.title)}, ${sqlEscape(row.created_at)});`
    );
  }

  for (const row of messages) {
    lines.push(
      `INSERT OR REPLACE INTO messages (id, session_id, role, content, created_at) VALUES (${sqlEscape(row.id)}, ${sqlEscape(row.session_id)}, ${sqlEscape(row.role)}, ${sqlEscape(row.content)}, ${sqlEscape(row.created_at)});`
    );
  }

  for (const row of memory) {
    lines.push(
      `INSERT INTO memory (id, user_id, summary, created_at, updated_at) VALUES (${sqlEscape(row.id)}, ${sqlEscape(row.user_id)}, ${sqlEscape(row.summary)}, ${sqlEscape(row.created_at)}, ${sqlEscape(row.updated_at)}) ON CONFLICT(user_id) DO UPDATE SET summary = excluded.summary, updated_at = excluded.updated_at;`
    );
  }

  lines.push("COMMIT;");

  const outputPath = resolve(process.cwd(), "migrations", "seed_from_appwrite.sql");
  await writeFile(outputPath, `${lines.join("\n")}\n`, "utf8");

  console.log(
    JSON.stringify(
      {
        output: outputPath,
        sessions: sessions.length,
        messages: messages.length,
        memory: memory.length,
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
